// To convert -ve value into +ve value
let temp = -10;

let weight = 70.4;

console.log(Math.abs(temp)); // 10

console.log(Math.round(weight)); // 70

console.log(Math.ceil(weight)); // 71 ⇒ Math.ceil() will always choose ↑ upper value

console.log(Math.floor(weight)); // 70 ⇒ Math.floor() will always choose ↓ lower value

console.log(Math.max(2, 5, 10, 8)); // Return max value from the given values

console.log(Math.min(-2, -1, 4, 10, 1)); // Return min value from the given values
