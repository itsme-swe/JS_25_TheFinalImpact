console.log(`Result after subtracting ${a} and ${b} is`, a - b); // Result after subtracting 10 and 5 is 5

console.log();

console.log(`Result after multiplying ${a} and ${b} is`, a * b); // Result after multiplying 10 and 5 is 50

console.log();

console.log(`Result after dividing ${a} with ${b} is`, a / b); // Result after dividing 10 with 5 is 2

console.log();

console.log(`Modulo of ${a} by ${b} is`, a % b); // Modulo of 10 by 5 is 0

console.log();

a++;
